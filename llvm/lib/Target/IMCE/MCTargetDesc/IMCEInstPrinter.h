#ifndef LLVM_LIB_TARGET_IMCE_MCTARGETDESC_IMCEINSTPRINTER_H
#define LLVM_LIB_TARGET_IMCE_MCTARGETDESC_IMCEINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"
#include "llvm/MC/MCRegister.h"
#include <cstdint>

namespace llvm {

class MCAsmInfo;
class MCOperand;

class IMCEInstPrinter : public MCInstPrinter {
public:
  IMCEInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII, const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  // Automatically generated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address, const MCSubtargetInfo &STI,
                        raw_ostream &O);
  static const char *getRegisterName(MCRegister RegNo);

  // Override MCInstPrinter.
  // void printRegName(raw_ostream &O, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot, const MCSubtargetInfo &STI,
                 raw_ostream &O) override;

private:
  // Print the given operand.
  void printOperand(const MCInst *MI, int OpNum, const MCSubtargetInfo &STI, raw_ostream &O);
  // static void printOperand(const MCOperand &MO, const MCAsmInfo *MAI, raw_ostream &O);

  void printJumpOperand(const MCInst *MI, unsigned OpNo,
                        const MCSubtargetInfo &STI, raw_ostream &O);
  void printBranchOperand(const MCInst *MI, uint64_t Address, unsigned OpNo,
                          const MCSubtargetInfo &STI, raw_ostream &O);

};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_IMCE_MCTARGETDESC_IMCEINSTPRINTER_H
